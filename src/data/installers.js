// Compute value scores across all installers (price 40%, warranty 30%, speed 15%, reviews 15%)
export function withScores(list) {
  const prices = list.map(i => i.price);
  const min = Math.min(...prices), max = Math.max(...prices);
  return list.map(it => {
    const priceScore = 10 * (1 - (it.price - min) / (max - min));
    const revScore   = ((it.rating - 4.5) / 0.5) * 10;
    const raw = priceScore * 4 + it.warrantyScore * 3 + it.speedScore * 1.5 + revScore * 1.5;
    return { ...it, score: Math.round(raw) };
  });
}

